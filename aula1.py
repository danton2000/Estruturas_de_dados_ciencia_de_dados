# Pratica 3
lista_preco = [45.0, 12.5, 89.9, 55.2]

# Printando a lista ordenada
lista_preco.sort()

print(f"Lista ordenada: {lista_preco}")

# Printando a lista ordenada reversa
lista_preco.reverse()

print(f"Lista ordenada (reversa): {lista_preco}")

